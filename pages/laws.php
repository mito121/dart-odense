    <div class="wrapper">
        <h1 class="mb-8">Love & vedtægter</h1>

        <div class="law">
            <h2>§ l Foreningens navn og hjemsted:</h2>
            <p>Foreningens navn er Dart Odense. Dens hjemsted er Odense kommune.</p>
        </div>

        <div class="law">
            <h2>§ 2 Foreningens formål:</h2>
            <p>
                Foreningens formål er at dyrke dartspil, samt at udbrede kendskabet til denne idrætsgren.
                Foreningsarbejdet skal give, især børn og unge under 25 år, mulighed for virksomhed og kammeratligt
                samvær i deres fritid.
            </p>
            <p>Dart Odenses sportslige ambitioner.</p>

            <ol class="abc">
                <li>
                    Den sportslige ambition tages løbende op til vurdering af bestyrelsen, eller af et udvalg nedsat af
                    bestyrelsen som skal varetage klubbens interesser og tage et realistisk hensyn til spillernes niveau
                    nu og her. ambitionerne skal meldes ud når holdene er sat før sæsonstart.
                </li>

                <li>
                    Det skal være en sportslig ambition at fremme sporten udadtil og tiltrække nye medlemmer
                </li>
                <li>
                    Der bør være en sportslig ambition om at både senior-/ og junior-spillere har mulighed for at
                    udvikle og
                    dygtiggøre sig i klubben, det være i form af gamle som nye træningsmetoder
                </li>

            </ol>
        </div>

        <div class="law">
            <h2>§ 3 Medlemskab af organisation:</h2>
            <p>
                Foreningen er medlem af Samvirkende idrætsforeninger i Odense (SIKO) og Dansk Dart Union, og er
                underkastet disses love og vedtægter.
            </p>
        </div>

        <div class="law">
            <h2>§ 4 Optagelse af medlemmer:</h2>
            <p>
                Som aktivt medlem kan bestyrelsen optage enhver. Foreningen skal have en registreret medlemskreds. Nye
                medlemmer (aktive og passive) skal gøres bekendt med klubbens love og vedtægter.
            </p>
        </div>

        <div class="law">
            <h2>§ 5 Kontingent:</h2>
            <p>
                Foreningens kontingent og kontingenttyper fastsættes løbende af bestyrelsen, Kontingentet opkræves og
                betales kvartalsvis forud.
            </p>
            <p>Æresmedlemmer skal ikke betale kontingent.</p>
            <p>
                Bestyrelsen kan kompenserer medlemmer der yder en ekstra indsats for klubben med nedsat eller frit
                kontingent.
            </p>
        </div>

        <div class="law">
            <h2>§ 6 Udmeldelse og eksklusion:</h2>
            <p>
                Til gyldig udmeldelse kræves. at den sker skriftligt med mindst 14 dages varsel og at det pågældende
                medlem ikke på nogen måde overfor foreningen er i restance. Medlemmet har krav på skriftlig bekræftelse.
                Når et medlem er i kontingentrestance udover 2 måneder, bliver vedkommende automatisk eksluderet..
                Optagelse kan ske igen, mod betaling af gælden til foreningen. I øvrigt kan bestyrelsen ekskludere et
                medlem, når særlige forhold giver anledning dertil. Bestyrelsens beslutning kræver dog, at mindst 2/3 af
                bestyrelsens medlemmer har stemt for eksklusionen. Vedkommende skal, inden bestyrelsen træffer sin
                afgørelse, have haft lejlighed til at fremføre sit forsvar, og vedkommende kan kræve, at spørgsmålet om
                eksklusion afgøres på den førstkommende generalforsamling.
            </p>
            <p>
                Når en eksklusion skal behandles på en generalforsamling, har vedkommende medlem krav på at få
                meddelelse herom senest 8 dage før generalforsamlingen afholdes, ligesom vedkommende har adgang til
                denne, med ret til at forsvare sig. Spørgsmålet om eksklusion skal optages som et særskilt punkt på
                dagsordenen. Generalforsamlingsbeslutningen om eksklusion kræver majoritet, som ved ændring af klubbens
                love jf. § 15.
            </p>
            <p>
                et medlem der er ekskluderet i henhold til en generalforsamling, kan kun optages som medlem ved en ny
                generalforsamlings beslutning. Her kræves samme majoritet som ved beslutning af eksklusion.
            </p>
        </div>

        <div class="law">
            <h2>§ 7 Ordinær generalforsamling:</h2>
            <p>
                Generalforsamlingen har med de indskrænkninger, som disse love foreskriver, den højeste
            </p>
            <p>
                myndighed i alle foreningens anliggender. Stemmeret har alle aktive medlemmer, som ikke er i
                kontingentrestance. Stemmeretten kan kun udøves ved personligt fremmøde. Passive medlemmer har ikke
                stemmeret ved generalforsamlingen, ligesom disse ikke kan indløse DDU-Licens.
            </p>
            <p>
                Undtaget er dog oldboys stævner og oldboys rækken under DDU, her kan passive medlemmer af klubben
                deltage.
            </p>
            <p>
                Ordinær generalforsamling. der skal afholdes senest 15. Maj, indkaldes med mindst 3 ugers varsel ved
                skriftlig meddelelse til medlemmerne. Forslag, der ønskes behandlet på generalforsamlingen skal være
                bestyrelsen i hænde senest 14 dage før generalforsamlingens afholdelse, og disse forslag optages på
                dagsordenen, der bekendtgøres senest 8 dage før generalforsamlingen. på samme måde som ovennævnte.
            </p>
        </div>

        <div class="law">
            <h2>§ 8 Dagsorden:</h2>
            <p>Dagsordenen for den ordinære generalforsamling. skal mindst omfatte følgende punkter:</p>

            <ol>
                <li>Valg af dirigent</li>
                <li>Bestyrelsens beretning for det forløbne år.</li>
                <li>Fremlæggelse af regnskab for det forløbne år til godkendelse.</li>
                <li>Vedtagelse af budget samt fastlæggelse af kontingent for det kommende år.</li>
                <li>Indkomne forslag.</li>
                <li>Valg af formand.</li>
                <li>Valg af kasserer.</li>
                <li>Valg af 3 medlemmer til den øvrige bestyrelse.</li>
                <li>Valg af 2 suppleanter til bestyrelsen.</li>
                <li>Valg af 2 revisorer samt en revisorsuppleant.</li>
                <li>Valg af udvalg.</li>
                <li>Eventuelt.</li>
            </ol>
        </div>


        <div class="law">
            <h2>§ 9 Generalforsamlingens ledelse m. v:</h2>
            <p>Generalforsamlingen vælger en dirigent, der ikke må være medlem af bestyrelsen.</p>
            <p>
                Generalforsamlingen er altid beslutningsdygtig og dens beslutninger træffes ved simpelt flertal, dog
                undtaget
                de tilfælde, hvor der i disse love er nævnt andet. Hvis et medlem ønsker det, skal afstemningen være
                skriftlig. Alle afstemninger om eksklusion skal være skriftlige. Alle generalforsamlingens beslutninger
                føres til protokols, endvidere laves et mødereferat som ophænges i klublokalerne. Mødereferatet
                underskrives
                af dirigenten.
            </p>
        </div>

        <div class="law">
            <h2>§ 10 Ekstraordinær generalforsamling:</h2>
            <p>
                Ekstraordinær generalforsamling kan til enhver tid indkaldes af bestyrelsen, og skal indkaldes, når
                mindst
                25 % stemmeberettigede medlemmer skriftligt stiller krav herom til bestyrelsen, med oplysning om emne
                der
                ønskes behandlet. I sidstnævnte tilfælde skal generalforsamlingen afholdes senest 1 måned efter, at
                begæringen er fremsat overfor bestyrelsen. Om indkaldelsesmåde og myndighed gælder bestemmelserne i § 7.
                Den
                ekstraordinære generalforsamling er kun beslutningsdygtig når mindst l/3 af de Stemmeberettigede
                medlemmer
                er til stede, samt dem, der anmoder om generalforsamlingen. Dens beslutninger træffes ved simpelt
                flertal.
                Opnås sådant flertal på en generalforsamling, der ikke er beslutningsdygtig, indkaldes til en ny
                ekstraordinær generalforsamling, hvor beslutningen kan træffes uanset hvilket antal stemmeberettigede
                medlemmer der er til stede. En ekstraordinær generalforsamling, hvis formål er at vælge bestyrelsen, er
                altid beslutningsdygtig.
            </p>
        </div>

        <div class="law">
            <h2>§ 11 Bestyrelse – beføjelser – valg:</h2>
            <p>
                Bestyrelsen er foreningens daglige ledelse og repræsenterer foreningen i alle forhold. Bestyrelsen kan,
                i
                det omfang generalforsamlingen bemyndiger den til det, optage kreditter på klubbens vegne. Bestyrelsen
                består af en formand, en næstformand, en kasserer samt 2 medlemmer, der vælges for 2 år af gangen.
                Formanden
                og 1 bestyrelsesmedlem vælges i ulige år. Kasserer og 2 bestyrelsesmedlemmer vælges i lige år. Efter et
                valg
                vil bestyrelsen jf. § 12 konstituere sig selv med undtagelse af formand og kasserer, som vælges ved en
                særskilt afstemning.
            </p>
            <p>
                Suppleanter og revisor vælges for et år. Genvalg kan finde sted. Flertallet af bestyrelsen skal være
                myndige.
            </p>
        </div>

        <div class="law">
            <h2>§ 12 Konstituering – tegningsret:</h2>

            <p>
                Bestyrelsen konstituerer sig snarest, dog senest 14 dage efter generalforsamlingen, på et
                bestyrelsesmøde
                med sekretær. Bestyrelsen fastsætter selv sin forretningsorden. Bestyrelsen er kun beslutningsdygtig,
                når
                mindst halvdelen af dens medlemmer, heriblandt formanden eller næstformanden er tilstede, dog jf.§ 6. I
                formandens fravær indtræder næstformanden. Ved dennes fravær en af formanden udpeget stedfortræder.
            </p>
            <p>
                Formanden indkalder til bestyrelsesmøde så ofte han finder det påkrævet, eller hvis 2
                bestyrelsesmedlemmer
                forlanger det. Bestyrelsens beslutninger træffes ved et almindeligt stemmeflertal. Står stemmerne lige
                er
                formandens eller dennes afløser stemmeafgørende. Over bestyrelsens forhandlinger føres en protokol.
                Foreningen tegnes af formanden. Ved økonomiske dispositioner kræves underskrift af formanden,
                næstformanden
                eller kassereren. I alle større sager, såsom køb/salg/pantsætning af fast ejendom eller løsøre, tegnes
                foreningen af formanden, næstformanden og kassereren i forening.
            </p>
        </div>

        <div class="law">
            <h2>§ 13 Regnskab:</h2>

            <p>Foreningens regnskab er kalenderåret. Bestyrelsen skal inden I. februar afgive driftsregnskab for</p>
            <p>
                det foregående år og status pr. 31 december til revisorerne. Driftsregnskab og status forelægges den
                ordinære
                generalforsamling til godkendelse og skal være forsynet med revisorernes påtegning. Det reviderede
                regnskab
                sendes sammen med indkaldelsen til generalforsamlingen til medlemmerne jf. § 7
            </p>
        </div>

        <div class="law">
            <h2>§ 14 Revision:</h2>
            <p>
                Revisorerne skal hvert år, før den ordinære generalforsamling, gennemgå det samlede regnskab
            </p>
            <p>
                og påse, at beholdningerne er til stede. Driftsregnskab og status forsynes med en påtegning. Hver af
                revisorerne, har til enhver tid, adgang til at efterse regnskab og beholdning.
            </p>
        </div>

        <div class="law">
            <h2>§ 15 Vedtægtsændringer:</h2>

            <p>Forandring af lovene kan ske på enhver generalforsamling, når mindst 2/3 af de angivne stemmer er for
                forslaget.</p>
        </div>

        <div class="law">
            <h2>§ 16 Foreningens opløsning:</h2>
            <p>
                Bestemmelse om foreningens opløsning kan kun foretages på en i dette øjemed indkaldt
            </p>
            <p>
                generalforsamling. Til dennes beslutningsdygtighed kræves, at mindst ½ af klubbens stemmeberettigede
                medlemmer er tilstede, og til forslagets vedtagelse kræves, at mindst ¾ af de afgivne stemmer er for
                forslaget. Opnås sådant flertal på en generalforsamling, der ikke er beslutningsdygtig, indkaldes til en
                ny
                generalforsamling, hvor beslutningen kan træffes med ovennævnte stemmeflertal, uanset hvilket antal
                stemmeberettigede medlemmer, der er til stede. Eventuelle aktiver, herunder formue, fast ejendom og
                løsøre,
                skal fordeles til ungdomsarbejdet i Odense kommune
            </p>
        </div>

        <p class="last-revision">Sidst revideret 01.03.2020</p>

